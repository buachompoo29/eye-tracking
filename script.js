const jigsawSnapshots = {
  a: new Set(),
  b: new Set()
};

function updateJigsawGallery(video, centerX, centerY, side, t_aligned) {
  const fixationTime = Math.round(t_aligned);

  // ðŸ›‘ Skip if already snapped at this fixation time
  if (jigsawSnapshots[side].has(fixationTime)) {
    console.log(`âš ï¸ Skipping duplicate snapshot at ${fixationTime}s for side ${side}`);
    return;
  }

  // âœ… Mark this fixation as snapped
  jigsawSnapshots[side].add(fixationTime);

  console.log(`ðŸ§© Creating jigsaw snapshot for side: ${side}`);

  const galleryId = side === "a" ? 'jigsaw-gallery-a' : 'jigsaw-gallery-b';
  const gallery = document.getElementById(galleryId);
  if (!gallery) {
    console.warn(`âŒ Gallery container not found: #${galleryId}`);
    return;
  }

  const size = 200;
  const x = Math.max(0, Math.min(video.videoWidth - size, centerX - size / 2));
  const y = Math.max(0, Math.min(video.videoHeight - size, centerY - size / 2));

  console.log(`ðŸ“ Cropping at x=${x.toFixed(1)}, y=${y.toFixed(1)}, from video (${video.videoWidth}x${video.videoHeight})`);

  const canvas = document.createElement("canvas");
  canvas.width = size;
  canvas.height = size;

  const ctx = canvas.getContext("2d");
  try {
    ctx.drawImage(video, x, y, size, size, 0, 0, size, size);
    const videoTime = video.currentTime.toFixed(2);

    canvas.title = `ðŸ“½ï¸ Video Time: ${videoTime}s`;
    canvas.dataset.timestamp = fixationTime;
    canvas.style.cursor = "pointer";

    canvas.addEventListener("click", () => {
      video.currentTime = parseFloat(canvas.dataset.timestamp);
      video.play();
    });

    gallery.appendChild(canvas);
    console.log(`âœ… Snapshot appended to #${galleryId}`);
  } catch (err) {
    console.error(`âŒ Error drawing jigsaw snapshot:`, err);
  }
}


